function filterProducts(brand) {
  currentBrand = brand;
  ["all", "kirby", "rainbow"].forEach((b) => {
    const btn = document.getElementById("filter-" + b);
    if (btn) {
      btn.className =
        b === brand ? "btn btn-primary btn-sm" : "btn btn-outline btn-sm";
    }
  });
  const filtered =
    brand === "all"
      ? allProducts
      : brand === "kirby"
        ? kirbyProducts
        : rainbowProducts;
  renderCollections(sortProductList(filtered));
}

function sortProducts() {
  const sort = document.getElementById("sort-select").value;
  const list =
    currentBrand === "all"
      ? allProducts
      : currentBrand === "kirby"
        ? kirbyProducts
        : rainbowProducts;
  renderCollections(sortProductList(list, sort));
}

function sortProductList(list, sort) {
  const s = sort || document.getElementById("sort-select")?.value || "featured";
  return [...list].sort((a, b) => {
    if (s === "price-low") return a.price - b.price;
    if (s === "price-high") return b.price - a.price;
    if (s === "rating") return b.stars - a.stars || b.reviews - a.reviews;
    if (s === "reviews") return b.reviews - a.reviews;
    return 0;
  });
}

