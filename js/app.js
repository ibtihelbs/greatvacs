/* ═══════════════════════ STAR RATING ═══════════════════════ */
function renderStars(count, size = 14) {
  let html = "";
  for (let i = 0; i < 5; i++) {
    if (i < count) {
      html += `<svg class="star star-filled" width="${size}" height="${size}" viewBox="0 0 24 24"><polygon points="12 2 15.09 8.26 22 9.27 17 14.14 18.18 21.02 12 17.77 5.82 21.02 7 14.14 2 9.27 8.91 8.26 12 2"/></svg>`;
    } else {
      html += `<svg class="star star-empty" width="${size}" height="${size}" viewBox="0 0 24 24"><polygon points="12 2 15.09 8.26 22 9.27 17 14.14 18.18 21.02 12 17.77 5.82 21.02 7 14.14 2 9.27 8.91 8.26 12 2"/></svg>`;
    }
  }
  return html;
}

